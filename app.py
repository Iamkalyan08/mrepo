
# This is a simple "Hello, World!" program
print("Hello, World!")

# This defines a function that greets a user by name
def greet_user(name):
  """
  This function takes a name as input and prints a greeting.
  """
  print(f"Hello, {name}! Welcome to Python.")

# Call the function with a specific name
greet_user("Alice")

# You can also get input from the user and use it in the function
user_name = input("Enter your name: ")
greet_user(user_name)
